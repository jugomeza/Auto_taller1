public class Chassis {

    private String modelo;

    public Chassis(String modelo) {
        this.modelo = modelo;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    @Override
    public String toString() {
        String printBoard = "\n modelo del chassis: " + modelo;
        return printBoard;
    }
}
